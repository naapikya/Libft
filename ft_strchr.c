#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*t_s;

	t_s = (char *)s;
	while (*t_s != c)
	{
		if (*t_s == '\0')
			return (NULL);
		t_s++;
	}
	return (t_s);
}
