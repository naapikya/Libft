#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*t_b;
	size_t	i;

	i = 0;
	t_b = b;
	while (i < len)
	{
		t_b[i] = c;
		i++;
	}
	return (b);
}
